long long	ft_atoi_base(const char *s, int b)
{
	long long int	res;
	int				neg;

	res = 0;
	neg = 1;
	while ((*s > 0 && *s < 14) || *s == 32)
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			neg = -1;
		s++;
	}
	while ((*s >= 48 && *s <= (b + 47)) || (*s >= 65 && *s <= (b + 54)) ||
		(*s >= 97 && *s <= (b + 86)))
	{
		if (*s >= 48 && *s <= (b + 47))
			res = b * res + (*s - '0');
		else if (*s >= 65 && *s <= (b + 54))
			res = b * res + (*s - 55);
		else if (*s >= 97 && *s <= (b + 86))
			res = b * res + (*s - 87);
		s++;
	}
	return (neg * res);
}
