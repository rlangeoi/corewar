#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c,
		size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		*(unsigned char*)(dst + i) = *(unsigned char*)(src + i);
		if (*(unsigned char*)(src + i) == (unsigned char)c)
		{
			i++;
			dst = (unsigned char*)(dst + i);
			return (dst);
		}
		i++;
	}
	return (NULL);
}
